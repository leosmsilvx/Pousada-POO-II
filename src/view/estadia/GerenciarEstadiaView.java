    private void jtEstadiasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtEstadiasMouseClicked
        linha  = jtEstadias.getSelectedRow();
        if (linha != -1) {
            jtxIdEstadia.setText(jtEstadias.getValueAt(linha, 0).toString());
            jtxCheckin.setText(jtEstadias.getValueAt(linha, 1).toString());
            jtxCheckoutEsperado.setText(jtEstadias.getValueAt(linha, 2).toString());
            jtxCheckoutFinal.setText(jtEstadias.getValueAt(linha, 3).toString());
            jcbQuartos.setSelectedItem((Quarto) jtEstadias.getValueAt(linha, 4));
            jcbHospedes.setSelectedItem((Hospede) jtEstadias.getValueAt(linha, 5));
            jtxCheckin.setEditable(true);
            jtxCheckoutEsperado.setEditable(true);
            jtxCheckoutFinal.setEditable(true);
            jcbQuartos.setEnabled(true);
            jcbHospedes.setEnabled(true);   
            linha = -1;
        }
    }//GEN-LAST:event_jtEstadiasMouseClicked


