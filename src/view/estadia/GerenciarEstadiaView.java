    private void jbEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEditarActionPerformed
        Integer id = Integer.valueOf(jtxIdEstadia.getText());
        String strDataCheckin = jtxCheckin.getText();
        String strDataCheckoutEsperado = jtxCheckoutEsperado.getText();           
        String strDataCheckoutFinal = jtxCheckoutFinal.getText(); 
        Quarto quarto = (Quarto) jcbQuartos.getSelectedItem();
        Hospede hospede = (Hospede) jcbHospedes.getSelectedItem();
        if ((strDataCheckin.isEmpty()) || (strDataCheckoutEsperado.isEmpty()) || (quarto == null) || hospede == null) {
            JOptionPane.showMessageDialog(this, "Digite todos os campos", "Retorno Tela", JOptionPane.ERROR_MESSAGE);
        }
        else {
            try {
                EstadiaController controller = new EstadiaController();
                Estadia estadia = new Estadia();
                SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
                estadia.setDtCheckin(format.parse(strDataCheckin));
                estadia.setDtCheckoutEsperado(format.parse(strDataCheckoutEsperado));
                if(!strDataCheckoutFinal.isEmpty()){
                    estadia.setDtCheckoutFinal(format.parse(strDataCheckoutFinal));
                }
                estadia.setHospede(hospede);
                estadia.setQuarto(quarto);
                estadia.setIdEstadia(id);
                controller.edit(estadia);
                limparCampos();
                preencherTabela();
            } catch (Exception ex) {
                ex.printStackTrace();
            }
            JOptionPane.showMessageDialog(this, "ServiÃ§o Inserido com Sucesso!");
            limparCampos();
            preencherTabela();
        }
    }//GEN-LAST:event_jbEditarActionPerformed

