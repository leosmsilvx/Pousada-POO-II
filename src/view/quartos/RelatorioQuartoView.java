    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        CategoriaQuarto cat = (CategoriaQuarto) jcbTipo.getSelectedItem();
        QuartoController c = new QuartoController();
        List<Quarto> lista = c.findByCategoriaQuarto(cat);
        if(lista.isEmpty()){
            JOptionPane.showMessageDialog(this, "Nenhum quarto encontrado para o filtro");
            DefaultTableModel modeloTabela = (DefaultTableModel) jtQuartos.getModel();
            modeloTabela.setRowCount(0);
            return;
        }
        
        DefaultTableModel modeloTabela = (DefaultTableModel) jtQuartos.getModel();
        modeloTabela.setRowCount(0);
        for (Quarto q: lista) {
                modeloTabela.addRow(new Object[]{ 
                    q.getIdQuarto(),
                    q.getValor(),
                    q.getCategoria()});
            }
    }//GEN-LAST:event_jbBuscarActionPerformed
    
    private void preencherCombo(){
        List<CategoriaQuarto> lista = Arrays.asList(CategoriaQuarto.values());
        for (CategoriaQuarto c: lista)
            jcbTipo.addItem(c);
    }
    
