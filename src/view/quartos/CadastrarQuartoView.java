    private void preencherTabela(){
        QuartoController controller = new QuartoController();
        List<Quarto> lista = controller.findAll();
        if (lista.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhum Quarto Cadastrado", "Retorno Tela", JOptionPane.ERROR_MESSAGE);
        }
        else {
            DefaultTableModel modeloTabela = (DefaultTableModel) jtQuartos.getModel();
            modeloTabela.setRowCount(0);
            for (Quarto q: lista) {
                modeloTabela.addRow(new Object[]{ 
                    q.getIdQuarto(),
                    q.getValor(),
                    q.getCategoria()});
            }
        }
    }
    
    private void limparCampos(){
        jtxValor.setText("");
        jcbTipo.setSelectedIndex(0);
    }
    
    private void preencherCombo(){
        List<CategoriaQuarto> lista = Arrays.asList(CategoriaQuarto.values());
        for (CategoriaQuarto c: lista)
            jcbTipo.addItem(c);
    }
    
